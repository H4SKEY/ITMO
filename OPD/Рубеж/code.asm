ORG 0x400
max1: WORD 0
max2: WORD 0

curr_num: WORD $arr
tmp: WORD ?
curr_ind: WORD 0x3
tmp_ind: WORD ?
arr_el: WORD 13
mask: WORD 0xFFE0
neg_num: WORD 0x8000

START: 
  LD (curr_num)+
  ST tmp
  LD curr_ind
  INC
  ST curr_ind
  JUMP KR4
CONTINUE_START:
  LD tmp
  JUMP CHECK_RAZR
AFTER_CHECK_RAZR:
  ADD tmp
  CMP max1
  BNS SKIP
  BZS MAX2
  ST max1
  LD (curr_num)+
  ST max2
  JUMP CONTINUE
  
MAX2:
  LD (curr_num)+
  CMP max2
  BNS CONTINUE
  ST max2
  JUMP CONTINUE

CHECK_RAZR:
  AND mask
  BZS AFTER_CHECK_RAZR
  LD neg_num
  JUMP AFTER_CHECK_RAZR
  
KR4:
  SUB #0x4
  CMP #0x0
  BZS CONTINUE_START
  BNS SKIP
  JUMP KR4

SKIP:
  LD (curr_num)+
CONTINUE:
  LOOP arr_el
  JUMP START

